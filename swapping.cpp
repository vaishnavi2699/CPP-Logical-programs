// WAP to for swapping of two numbers.
#include<iostream>
using namespace std;
int main()
{
	int a=45,b=12,temp;
	cout<<"before swapping"<<"\n";
	cout<<"a value is: "<<a<<"\t";
	cout<<"b value is: "<<b<<"\t";
	temp=a;
	a=b;
	b=temp;
	cout<<"after swapping"<<"\n";
    cout<<"a value is: "<<a<<"\t";
    cout<<"b value is: "<<b;
return 0;
	
}
